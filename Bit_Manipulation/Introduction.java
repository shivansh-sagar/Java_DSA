package Bit_Manipulation;

public class Introduction {
    public static void main(String[] args){
        // Bit Manipulation
        int a =5, b=6;
        System.out.println(a&b);
        System.out.println(a|b);
        System.out.println(a^b);
        System.out.println(~2);
        System.out.println(a<<2);
        System.out.println(b>>1);
        System.out.println(7&1);
    }
}
